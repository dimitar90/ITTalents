package peasant_brigade.premises;

public class VegetableException extends Exception {

    /**
     * 
     */
    private static final long serialVersionUID = 1L;

    public VegetableException() {
        super();
    }

    public VegetableException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public VegetableException(String message, Throwable cause) {
        super(message, cause);
    }

    public VegetableException(String message) {
        super(message);
    }

    public VegetableException(Throwable cause) {
        super(cause);
    }

}
